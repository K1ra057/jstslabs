// variables.ts

// Объявление и инициализация переменных различных типов
let myString:string = "Привет, мир!";
let myNumber:number = 42;
let myBoolean:boolean = true;
let myAny:any = "Может быть любой тип";

// Вывод значений переменных в консоль
console.log("String:", myString);
console.log("Number:", myNumber);
console.log("Boolean:", myBoolean);
console.log("Any:", myAny);

// Объявление и инициализация массивов
let stringArray: string[] = ["Один", "Два", "Три"];
let numberArray: number[] = [1, 2, 3, 4, 5];

// Вывод значений массивов в консоль
console.log("String Array:", stringArray);
console.log("Number Array:", numberArray);
